    private void txtCustomerCashFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCustomerCashFocusLost
        // TODO add your handling code here:
        int totalCash = Integer.parseInt(lblTotalCash.getText());
        if(!txtCustomerCash.getText().isBlank()){
        int customerCash = Integer.parseInt(txtCustomerCash.getText());
            if(totalCash < customerCash){
                int rollBackCash = customerCash - totalCash;
                lblRollBackCash.setText(rollBackCash+"");
            }
        }
    }//GEN-LAST:event_txtCustomerCashFocusLost

    private void btnAddOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddOrderActionPerformed
        // TODO add your handling code here:
        if(lblIdOrder.getText().isEmpty()){
            Order order = this.getOrder();
            if(order!= null) {
                Order savedOrder = this.orderService.saveOrder(order);
                lblIdOrder.setText(savedOrder.getId()+"");
                JOptionPane.showMessageDialog(this, "In hoa don thanh cong!");
                OrderDetail orderDetail = new OrderDetail();
                for(int i = 0 ; i < listCards.size();i++){
                    OrderDetailId orderDetailId = new OrderDetailId();
                    orderDetailId.setOrder(order);
                    orderDetailId.setProducts(listCards.get(i));
                    orderDetail.setOrderDetailId(orderDetailId);
                    int quantity = Integer.valueOf(modelCards.getValueAt(i, 3).toString());
                    orderDetail.setQuantity(quantity);
                    OrderDetail savedOrderDetail = this.orderDetailService.saveOrderDetail(orderDetail);
                    if(savedOrderDetail != null){
                        System.out.println("Save thanh cong");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnAddOrderActionPerformed

