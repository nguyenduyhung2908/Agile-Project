    private void btnAddToCardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCardActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableProducts.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Ban chua chon san pham");
        }else{
            Products products = listProducts.get(selectedRow);
            if(listCards.contains(products)){
                int index = listCards.indexOf(products);
                int quantity  = Integer.valueOf(modelCards.getValueAt(index, 3)+"");
                modelCards.setValueAt(++quantity , index, 3);
                modelCards.setValueAt(quantity * products.getPrice(), index, 4);
            }else{
                listCards.add(products);
                loadCard(products, 1, products.getPrice());
            }
        }
    }//GEN-LAST:event_btnAddToCardActionPerformed

