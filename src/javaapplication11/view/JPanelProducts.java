    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        if(name != null){
            this.listProducts = this.productService.getAllProducts();
            List<Products> findProducts = this.getProductsByName(name);
            if(!findProducts.isEmpty()) {
                this.listProducts = findProducts;
                loadTable();
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed
    private Products validateProducts() {
        Products products = new Products();
        String name = validator.getString(txtName.getText(), lblNameMsg);
        String sPrice = validator.getString(txtPrice.getText(), lblPriceMsg);
        String description = validator.getString(txtDescription.getText(), lblDescriptionMsg);
        String categoryName = cbxCategories.getSelectedItem().toString();
        int price = -1;
        if (sPrice != null)
        try {
            price = Integer.parseInt(sPrice);
            lblPriceMsg.setText("");
        } catch (NumberFormatException ex) {
            lblPriceMsg.setText("Loi du lieu");
            lblPriceMsg.setForeground(Color.red);
        }
        if (null != name && null != description && -1 != price) {
            Categories categories = this.getCategories(categoryName);
            if (categories != null) {
                products.setName(name);
                products.setPrice(price);
                products.setDescription(description);
                products.setCategories(categories);
                products.setImage(this.pathImage);
                this.pathImage = "";
                return products;
            }
        }
        return null;
    }

    private void fillComboBox(List<Categories> list) {
        cbxCategories.removeAllItems();
        if (!list.isEmpty()) {
            for (Categories c : list) {
                cbxCategories.addItem(c.getName());
            }
        }
    }

    private Categories getCategories(String name) {
        for (Categories c : listCategories) {
            if (c.getName().equals(name)) {
                return c;
            }
        }
        return null;
    }

    private String getPathImage() {
        String absolutePath = "";
        FileFilter fileFilter = new FileNameExtensionFilter("image", ImageIO.getReaderFileSuffixes());
        JFileChooser fileChooser = new JFileChooser(new File("D:\\FPT Polytechnic\\Agile\\DemoImages\\"));
        fileChooser.setFileFilter(fileFilter);
        int chooser = fileChooser.showOpenDialog(jPanel1);
        if (chooser == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            absolutePath = file.getAbsolutePath();
        }
        return absolutePath;
    }
    private void resetTextField(){
        txtName.setText("");
        txtDescription.setText("");
        txtPrice.setText("");
        lblNameMsg.setText("");
        lblDescriptionMsg.setText("");
        lblPriceMsg.setText("");
        cbxCategories.setSelectedIndex(0);
        lblImage.setIcon(null);
    }
    private void resetMsgField(){
        lblNameMsg.setText("");
        lblDescriptionMsg.setText("");
        lblPriceMsg.setText("");
    }
    private List<Products> getProductsByName(String name){
        List<Products> findProducts = new ArrayList<>();
        for(Products p : listProducts){
            if(p.getName().contains(name)){
                findProducts.add(p);
            }
        }
        return findProducts;
    }
  /**
     * @param args the command line arguments
     */
    private void loadTable() {
        modelTable.setNumRows(0);
        if (listProducts != null) {
            for (Products p : listProducts) {
                modelTable.addRow(new Object[]{
                    p.getId(),
                    p.getName(),
                    validator.formatNumber(p.getPrice()),
                    p.getDescription(),
                    p.getCategories().getName(),
                    p.getImage()
                });
            }
        }
    }

