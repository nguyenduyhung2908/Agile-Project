    private void tableProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductsMouseClicked
        // TODO add your handling code here:
        this.resetMsgField();
        int selectedRow = tableProducts.getSelectedRow();
        if (selectedRow != -1) {
            Products products = listProducts.get(selectedRow);
            txtName.setText(products.getName());
            txtPrice.setText(products.getPrice() + "");
            txtDescription.setText(products.getDescription());
            cbxCategories.setSelectedItem(products.getCategories().getName());
            ImageIcon imageIcon = new ImageIcon(products.getImage());
            Image image = imageIcon.getImage().getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), Image.SCALE_SMOOTH);
            lblImage.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_tableProductsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableProducts.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Chua chon san pham");
        } else {
            int confirm = JOptionPane.showConfirmDialog(jPanel1, "Ban chac chan muon sua san pham nay?");
            if (confirm == JOptionPane.YES_OPTION) {
                Products product = this.validateProducts();
                Products selectProduct = listProducts.get(selectedRow);
                selectProduct.setName(product.getName());
                selectProduct.setPrice(product.getPrice());
                selectProduct.setCategories(product.getCategories());
                selectProduct.setDescription(product.getDescription());
                if (!(product.getImage().isBlank())) {
                    selectProduct.setImage(product.getImage());
                }
                boolean isUpdated = this.productService.updateProducts(selectProduct);
                if (isUpdated) {
                    listProducts = this.productService.getAllProducts();
                    loadTable();
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableProducts.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Chua chon san pham");
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Ban chac chan muon xoa san pham nay?");
            if (confirm == JOptionPane.YES_OPTION) {
                Products products = listProducts.get(selectedRow);
                boolean isUpdated = this.productService.deleteProducts(products);
                if (isUpdated) {
                    modelTable.removeRow(selectedRow);
                    this.resetTextField();
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

